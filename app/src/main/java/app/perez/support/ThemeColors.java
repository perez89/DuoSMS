package app.perez.support;

public class ThemeColors {
	String background;
	String leftShadowColor;
	String leftColor;
	String rightShadowColor;
	String rightColor;
	
	public ThemeColors(String background, String leftShadowColor, String leftColor, String rightShadowColor, String rightColor){
		this.background = background;
		this.leftShadowColor = leftShadowColor;
		this.leftColor = leftColor;
		this.rightShadowColor = rightShadowColor;
		this.rightColor = rightColor;
	}
}
